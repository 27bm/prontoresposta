// Função para obter a pontuação para exercícios de barra/apoio
export const getBarScore = (gender: Gender, ageRange: AgeRange, value: number): number => {
  const isYounger = ageRange === 'up_to_27' || ageRange === '28_to_35';
  
  if (isYounger) {
    // Barra ou isometria
    if (gender === 'male') {
      return malePullUpScores[ageRange][value] || 0;
    } else {
      return femaleIsometryScores[ageRange][value] || 0;
    }
  } else {
    // Apoio
    if (gender === 'male') {
      return malePushUpScores[ageRange][value] || 0;
    } else {
      return femalePushUpScores[ageRange][value] || 0;
    }
  }
};

// Função para obter a pontuação para abdominais
export const getSitupScore = (ageRange: AgeRange, value: number): number => {
  return situpScores[ageRange][value] || 0;
};

// Função para obter a pontuação para corrida
export const getRunningScore = (gender: Gender, ageRange: AgeRange, distance: number): number => {
  if (gender === 'male') {
    return maleRunningScores[ageRange][distance] || 0;
  } else {
    return femaleRunningScores[ageRange][distance] || 0;
  }
};
